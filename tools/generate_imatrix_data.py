def fit_bytes(text: str, max_bytes: int) -> str:
    b = text.encode("utf-8")
    if len(b) <= max_bytes:
        return text
    b = b[:max_bytes]
    while b:
        try:
            return b.decode("utf-8")
        except UnicodeDecodeError:
            b = b[:-1]
    return ""


def mega_para(sents: list[str], cycles: int) -> str:
    acc: list[str] = []
    for _ in range(cycles):
        pool = list(sents)
        random.shuffle(pool)
        acc.extend(pool)
    return " ".join(acc)


def tune_cycles(pool: list[str], byte_budget: int, start: int = 2, end: int = 120) -> str:
    lo, hi = start, end
    best = fit_bytes(mega_para(pool, start), byte_budget)
    while lo <= hi:
        mid = (lo + hi) // 2
        text = fit_bytes(mega_para(pool, mid), byte_budget)
        if text and len(text.encode("utf-8")) <= byte_budget:
            best = text
            lo = mid + 1
        else:
            hi = mid - 1
    return best


